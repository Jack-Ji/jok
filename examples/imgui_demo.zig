pub fn init(ctx: *jok.Context) anyerror!void {
    std.log.info("game init", .{});

    // init imgui
    try imgui.init(ctx);

    regular_font = try imgui.loadFontAwesome(30, true, true);
    solid_font = try imgui.loadFontAwesome(30, false, true);
}

fn printIcons(column_size: usize) void {
    if (imgui.beginTable(
        "table",
        @intCast(c_int, column_size),
        .{ .flags = imgui.c.ImGuiTableFlags_Borders },
    )) {
        var count: usize = 0;
        for (codepoints) |c, i| {
            if (imgui.isCharRenderable(c)) {
                if (count % column_size == 0) {
                    imgui.tableNextRow(0, 50);
                }
                _ = imgui.tableNextColumn();
                _ = imgui.text(codepoint_names[i].ptr);
                imgui.newLine();
                imgui.newLine();
                imgui.sameLine(.{ .offset_from_start_x = imgui.getColumnWidth(imgui.getColumnIndex()) - 40, .spacing = 10 });
                _ = imgui.text(c.ptr);
                count += 1;
            }
        }
        imgui.endTable();
    }
}

pub fn loop(ctx: *jok.Context) anyerror!void {
    while (ctx.pollEvent()) |e| {
        _ = imgui.processEvent(e);

        switch (e) {
            .key_up => |key| {
                switch (key.scancode) {
                    .escape => ctx.kill(),
                    else => {},
                }
            },
            .quit => ctx.kill(),
            else => {},
        }
    }

    const S = struct {
        var f: f32 = 0.0;
        var counter: i32 = 0;
        var show_demo_window = true;
        var show_another_window = true;
        var show_plot_demo_window = true;
        var show_nodes_demo_window = true;
        var clear_color = [3]f32{ 0.45, 0.55, 0.6 };
    };

    try ctx.renderer.clear();
    imgui.beginFrame();
    defer imgui.endFrame();

    var mouse_state = ctx.getMouseState();
    imgui.setNextWindowPos(.{
        .x = @intToFloat(f32, mouse_state.x + 10),
        .y = @intToFloat(f32, mouse_state.y + 10),
    }, .{});
    if (imgui.begin("mouse context", null, imgui.c.ImGuiWindowFlags_NoTitleBar)) {
        imgui.text("You're here!");
    }
    imgui.end();

    if (imgui.begin("Hello, world!", null, null)) {
        imgui.text("This is some useful text");
        imgui.textUnformatted("some useful text");
        _ = imgui.checkbox("Demo Window", &S.show_demo_window);
        _ = imgui.checkbox("Another Window", &S.show_another_window);
        _ = imgui.checkbox("Plot Demo Window", &S.show_plot_demo_window);
        _ = imgui.checkbox("Nodes Demo Window", &S.show_nodes_demo_window);
        _ = imgui.sliderFloat("float", &S.f, 0, 1, .{});
        if (imgui.colorEdit3("clear color", &S.clear_color, null)) {
            try ctx.renderer.setColorRGB(
                @floatToInt(u8, @floor(S.clear_color[0] * 255.0)),
                @floatToInt(u8, @floor(S.clear_color[1] * 255.0)),
                @floatToInt(u8, @floor(S.clear_color[2] * 255.0)),
            );
        }
        if (imgui.button("Button", null))
            S.counter += 1;
        imgui.sameLine(.{});
        imgui.text("count = %d", S.counter);
    }
    imgui.end();

    if (S.show_demo_window) {
        imgui.showDemoWindow(&S.show_demo_window);
    }

    if (S.show_another_window) {
        if (imgui.begin("Another Window", &S.show_another_window, null)) {
            imgui.text("Hello from another window!");
            if (imgui.button("Close Me", null))
                S.show_another_window = false;
        }
        imgui.end();
    }

    if (S.show_plot_demo_window) {
        imgui.ext.plot.showDemoWindow(&S.show_plot_demo_window);
    }

    if (S.show_nodes_demo_window) {
        if (imgui.begin("Nodes Demo Window", &S.show_nodes_demo_window, 0)) {
            imgui.ext.nodes.beginNodeEditor();
            imgui.ext.nodes.beginNode(-1);
            imgui.dummy(.{ .x = 80, .y = 45 });
            imgui.ext.nodes.endNode();
            imgui.ext.nodes.endNodeEditor();
        }
        imgui.end();
    }

    const column_size = 5;
    if (imgui.begin("font-awesome-regular", null, 0)) {
        imgui.pushFont(regular_font);
        printIcons(column_size);
        imgui.popFont();
    }
    imgui.end();

    if (imgui.begin("font-awesome-solid", null, 0)) {
        imgui.pushFont(solid_font);
        printIcons(column_size);
        imgui.popFont();
    }
    imgui.end();
}

pub fn quit(ctx: *jok.Context) void {
    _ = ctx;
    std.log.info("game quit", .{});

    imgui.deinit();
}
